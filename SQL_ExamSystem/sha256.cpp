char charToHex(char val)
{
	if(val < 10)
	{
		return val + '0';
	}

	return (val - 10) + 'a';
}

void byteToHex(char bytes[2], uint8 byte)
{
	bytes[0] = charToHex((byte & 0xF0) >> 4);
	bytes[1] = charToHex(byte & 0x0F);
}

void byte_array_to_str(uint8* bytes, char* str, int len)
{
	for(int i = 0; i < len; i++)
	{
		byteToHex(str + (i*2), bytes[i]);
	}
}


